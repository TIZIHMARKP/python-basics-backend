
# ====================  Return Keyword In Python =====================
# The return statement gives us a feedback of what is being executed in a function

def myFunction():
    return 5+4

def add_numbers(num1, num2):
    return num1 + num2

def add_numbers1(num1, num2):
    return num1 + num2

num1 = int(input("Enter first number: "))
num2 = int(input("Enter second number: "))

print(myFunction())
print(add_numbers(20, 20))
print(add_numbers1(num1, num2))

